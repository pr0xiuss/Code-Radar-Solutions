#include <stdio.h>

int main() {
    int num,s;
    scanf("%d",&num);
    for (int i=1;i<=num;i++){
        for (s=1;s<=num=-i;s++){
            printf(" ");
        }
        for (num=1;num<=i;num++){
            printf("%d ",num);
        }
        printf("\n");
    }
    return 0;
}